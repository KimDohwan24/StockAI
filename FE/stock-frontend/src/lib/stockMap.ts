// 코드 → 이름 매핑 (페이지 표시용)
export const STOCK_CODE_TO_NAME: Record<string, string> = Object.fromEntries(
  Object.entries(STOCK_NAME_TO_CODE).map(([name, code]) => [code, name])
);

// 코드에 대응하는 첫 번째 이름을 가져오는 헬퍼
export function getStockName(code: string): string {
  return STOCK_CODE_TO_NAME[code] || code;
}

// 이름에 대응하는 코드를 찾는 헬퍼 (정확 매칭)
export function getStockCode(name: string): string | undefined {
  return STOCK_NAME_TO_CODE[name.trim()];
}

// 부분 검색 헬퍼: 입력값으로 시작하거나 포함하는 종목명 목록 반환
export function searchStockNames(query: string): { name: string; code: string }[] {
  const trimmed = query.trim();
  if (!trimmed) return [];
  const results: { name: string; code: string }[] = [];
  const seen = new Set<string>();
  for (const [name, code] of Object.entries(STOCK_NAME_TO_CODE)) {
    if (name.includes(trimmed) && !seen.has(code)) {
      seen.add(code);
      results.push({ name, code });
    }
  }
  return results;
}
